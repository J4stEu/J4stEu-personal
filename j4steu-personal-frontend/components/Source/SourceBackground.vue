<script>
export default {
  props: {
    mouseMoveEvent: {
      type: Object,
      default: () => {}
    }
  },
  watch: {
    mouseMoveEvent () {
      this.onBackgroundMouseMove();
    }
  },
  methods: {
    onBackgroundMouseMove () {
      const e = this.mouseMoveEvent.e;
      const backgroundChildren = Array.from(this.$refs.instancesContainer.children);
      backgroundChildren.forEach((element, index) => {
        let speed;
        switch (index) {
          case 0:
            speed = -2;
            break;

          case 1:
            speed = -1;
            break;

          case 2:
            speed = -2;
            break;
        }

        const x = (window.innerWidth - e.pageX * speed) / 100;
        const y = (window.innerHeight - e.pageY * speed) / 100;

        element.style.transform = `translateX(${x}px) translateY(${y}px)`;
      });
    }
  }
};
</script>

